.macro CowRange(spriteX, spriteY, origX, origY, mask) {
range:
    ldy #0 
range1: 
    iny 
    dex 
    cpx spriteX
    beq beamon 
    cpy #30 
    bne range1 

    ldy #0 
range2: 
    iny 
    inx 
    cpx spriteX
    beq beamon 
    cpy #50 
    bne range2

    jsr landcow 
beamon:
    lda #mask 
    and $d015
    cmp #mask 
    bne landcow
    dec spriteY 
    inc $d020 
    // We're gonna do this a bunch 
    ldy #00 
makesound:
    ldx #15
    stx $d418
    ldx #0
    stx $d418 
    iny 
    cpy #10 
    bne makesound 
    jsr done  
landcow:
    // Make sure cow is at original starting place 
    lda #origX
    sta spriteX
    lda #origY 
    sta spriteY  
    //jmp done 
}



