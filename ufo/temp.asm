// Be warned, this is some of the messiest most haphazard code I've ever written 
// Abduct the cow!  
BasicUpstart2(start) // We start at $0801 

.const spr0_x = $d000 
.const spr0_y = $d001 
.const spr1_x = $d002 
.const spr1_y = $d003 

start:
    ldx #$00
    stx $d020
    stx $d021 
loadtitle:  
    jsr $e544
    ldx #$00
ltloop:
    lda title+2,x // Load the first bank of the map 
    sta $0400,x
    lda title+$3ea,x
    sta $d800,x     // Load first color bank 
    
    lda title+$102,x
    sta $0500,x
    lda title+$4ea,x
    sta $d900,x

    lda title+$202,x
    sta $0600,x
    lda title+$5ea,x
    sta $da00,x

    lda title+$2ea,x
    sta $06e8,x
    lda title+$6d2,x
    sta $dae8,x
    inx
    bne ltloop
    // Check if player fires to move to next screen 
titleloop:
    lda $dc00
    and #%00010000
    bne titleloop 

// The squidlet homeworld 
hwscreen:
    ldx #$0a
    stx $d021 
    jsr $e544
    ldx #$00
lhwloop:
    lda homeworld+2,x // Load the first bank of the map 
    sta $0400,x
    lda homeworld+$3ea,x
    sta $d800,x     // Load first color bank 
    
    lda homeworld+$102,x
    sta $0500,x
    lda homeworld+$4ea,x
    sta $d900,x

    lda homeworld+$202,x
    sta $0600,x
    lda homeworld+$5ea,x
    sta $da00,x

    lda homeworld+$2ea,x
    sta $06e8,x
    lda homeworld+$6d2,x
    sta $dae8,x
    inx
    bne lhwloop
    // Check if player fires to move to next screen 
hwloop:
    lda $dc00
    and #%00010001
    bne hwloop 

mooscreen1:
    // Start game mode 
    ldx #$06
    stx $d020
    ldx #$00
    stx $d021 
    jsr $e544 

    // Enable sprites 0 (UFO) and 1 (COW)
    lda #%00000011
    sta $d015 

    // Set multicolor mode for both UFO and COW
    lda #%00000011
    sta $d01c

    // Sprite 0 is at $2000, so set pointer to point to it
    lda #$80
    sta $07f8 
    // Sprite 1 is at $2100, so set pointer to point to it
    lda #$84
    sta $07f9 

    // Set ufo position
    lda #$a0 
    sta spr0_x 
    lda #$64 
    sta spr0_y 
    // Set cow position
    lda #$50
    sta spr1_x
    lda #$d5 
    sta spr1_y 

    // Global multicolor sprite colors 
    lda #$04 // Purple         
    sta $d025
    lda #$06 // Blue 
    sta $d026 
    // Set ufo color
    lda #$0e // Light Blue 
    sta $d027
    // Set cow color
    lda #$01 // White 
    sta $d028 

    // Load the first mooworld into character memory 
loadmap:  
    jsr $e544
    ldx #$00
lmloop:
    lda mooworld+2,x // Load the first bank of the map 
    sta $0400,x
    lda #1 
    sta $d800,x     // Load first color bank 
    
    lda mooworld+$102,x
    sta $0500,x
    lda mooworld+$4ea,x
    sta $d900,x

    lda mooworld+$202,x
    sta $0600,x
    lda mooworld+$5ea,x
    sta $da00,x

    lda mooworld+$2ea,x
    sta $06e8,x
    lda mooworld+$6d2,x
    sta $dae8,x
    inx
    bne lmloop 

    // Display victory text when we win the game 
wewon: 
    lda haswon 
    cmp #$01 
    bne loop 
drawtext:
    ldx #$06
    stx $d020 
    ldx #1 
    stx $0286
    //jsr $e544   
    ldx #$00
drawloop:
    lda msg,x      
    sta $0428,x
    inx            
    cpx #40        
    bne drawloop   
    


    // Main loop
loop:
delay:
    lda #$ff           
    cmp $d012          
    bne delay  

// Check if cow and ufo are colliding 
checkcol: 
    lda #%00000011
    cmp $d01e 
    bne moveup 
    // Disable the cow sprite 
    lda #%00000001
    sta $d015
    lda #0
    sta spr1_x
    sta spr1_y
    lda #1 
    sta haswon // We won the game! 
    jmp wewon  

// Controls         
moveup: 
    lda $dc00          
    and #%00000001     
    bne movedown       
    dec $d001          
movedown:
    lda $dc00
    and #%00000010     
    bne moveleft
    inc $d001          
moveleft:
    lda $dc00
    and #%00000100
    bne moveright 
    dec $d000          
checkbitleft:          
    ldx $d000 
    cpx #255
    bne leftbounds
    lda #0
    sta $d010
leftbounds: 
    ldx $d000
    cpx #1
    bne moveright
    ldx $d010
    cpx #0
    bne moveright
    lda #%00000001
    sta $d010
    ldx #89             
    stx $d000
moveright:     
    lda $dc00
    and #%00001000
    bne button
    inc $d000
checkbitright:          
    ldx $d000                            
    cpx #0              
    bne rightbounds     
    lda #%00000001      
    sta $d010           
rightbounds:
    ldx $d010
    cpx #%00000001      
    bne button         
    ldx $d000           
    cpx #89             
    bne button
    lda #0              
    sta $d010           
    ldx #$01            
    stx $d000           
button:
    lda $dc00
    and #%00010000
    beq done1 
    jmp landcow
done1: 
    // First check if the cow is even there and enabled 
    lda #%00000011
    cmp $d015 
    beq cowthere
    jmp done 
    
cowthere: 
    ldx spr0_x  // See if we're at the same x as cow 
    cpx spr1_x
    beq beamon
range:

    ldy #0 
range1: 
    iny 
    dex 
    cpx spr1_x
    beq beamon 
    cpy #30 
    bne range1 

    ldy #0 
range2: 
    iny 
    inx 
    cpx spr1_x
    beq beamon 
    cpy #50 
    bne range2

    jsr landcow 
beamon:
    dec spr1_y 
    inc $d020 
    // We're gonna do this a bunch 
    ldy #00 
makesound:
    ldx #15
    stx $d418
    ldx #0
    stx $d418 
    iny 
    cpy #10 
    bne makesound 
    jsr done  
landcow:
    // Make sure cow is at original starting place 
    lda #$50
    sta spr1_x
    lda #$d5 
    sta spr1_y 

done:
    // main loop 
    jmp loop

    // UFO sprite data 
    * = $2000
    .byte $00,$00,$00,$00,$00,$00,$00,$00
    .byte $00,$00,$00,$00,$00,$00,$00,$00
    .byte $00,$00,$00,$aa,$00,$01,$dd,$c0
    .byte $0a,$aa,$a0,$2a,$aa,$a8,$15,$55
    .byte $54,$3f,$ff,$fc,$2a,$aa,$a8,$0a
    .byte $aa,$a0,$00,$55,$00,$00,$00,$00
    .byte $00,$00,$00,$00,$00,$00,$00,$00
    .byte $00,$00,$00,$00,$00,$00,$00,$8e

     // Cow sprite data 
     * = $2100 
    .byte $00,$00,$00,$00,$00,$00,$00,$00
    .byte $00,$00,$00,$00,$00,$00,$00,$00
    .byte $00,$00,$00,$00,$00,$82,$00,$00
    .byte $28,$00,$00,$28,$00,$00,$28,$00
    .byte $00,$2a,$af,$e0,$2a,$ab,$88,$2a
    .byte $aa,$88,$0a,$aa,$88,$0a,$ba,$88
    .byte $0a,$fe,$88,$02,$00,$80,$02,$00
    .byte $80,$02,$00,$80,$02,$00,$80,$81

    // Squidlet sprite data
    * = $2200 
    .byte $00,$00,$00,$00,$00,$00,$01,$55
    .byte $50,$01,$55,$50,$01,$95,$90,$01
    .byte $95,$90,$01,$95,$90,$01,$95,$90
