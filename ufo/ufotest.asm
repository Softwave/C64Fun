BasicUpstart2(start)

* = $4000

.const spr0_x = $d000 
.const spr0_y = $d001 
.const spr1_x = $d002 
.const spr1_y = $d003 

start:
    lda screen_001
    sta $d020
    lda screen_001+1
    sta $d021
    lda #$15
    sta $d018

    ldx #$00
loadmap:
    lda screen_001+2,x
    sta $0400,x
    lda screen_001+$3ea,x
    sta $d800,x

    lda screen_001+$102,x
    sta $0500,x
    lda screen_001+$4ea,x
    sta $d900,x

    lda screen_001+$202,x
    sta $0600,x
    lda screen_001+$5ea,x
    sta $da00,x

    lda screen_001+$2ea,x
    sta $06e8,x
    lda screen_001+$6d2,x
    sta $dae8,x
    inx
    bne loadmap

    //
    // Enable sprites 0 (UFO) and 1 (COW)
    lda #%00000011
    sta $d015 

    // Set multicolor mode for both UFO and COW
    lda #%00000011
    sta $d01c

    // Sprite 0 is at $2000, so set pointer to point to it
    lda #$80
    sta $07f8 
    // Sprite 1 is at $2100, so set pointer to point to it
    lda #$84
    sta $07f9 

    // Set ufo position
    lda #$a0 
    sta spr0_x 
    lda #$64 
    sta spr0_y 
    // Set cow position
    lda #$50
    sta spr1_x
    lda #$d5 
    sta spr1_y 

    // Global multicolor sprite colors 
    lda #$04 // Purple         
    sta $d025
    lda #$06 // Blue 
    sta $d026 
    // Set ufo color
    lda #$0e // Light Blue 
    sta $d027
    // Set cow color
    lda #$01 // White 
    sta $d028 

    // Main loop
loop:
    jmp loop 


